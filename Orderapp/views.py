@api_view(['POST'])
@authentication_classes(authentication.TokenAuthentication)
@permission_classes(permissions.IsAuthenticated)

def checkout(request):
    srialzer = OrderSerilazer(data = request.data)
    if srialzer.is_valid():
        stripe.api_key = settings.STRIPE_SECRET_KEY
        paid_amount = sum(item.get('quantity') * item.get('product').price for item in srialzer.validated_data['items'])

        try:
            charging = stripe.Charge.create(
                amount = int(paid_amount * 1000),
                currency = 'INR',
                description = 'Charge from djackets',
                source = srialzer.validated_data['stipe_token']
            )

            srialzer.save(user = request.user, paid_amount = paid_amount)
            return Response(srialzer.data, status = status.HTTP_201_CREATED)
        except Exception as e:
            return Response(srialzer.data, status = status.HTTP_400_BAD_REQUEST)
    return Response(srialzer.data, status = status.HTTP_400_BAD_REQUEST)



class OrederView(APIView):
    authentication_classes = [authentication.TokenAuthentication]
    permission_classes = [permissions.IsAuthenticated]

    def get(self, request, format=None):
        order = Order.objects.filter(user=request.user)
        serialozer = OrderSerilazer(order, many=True)
        return Response(serialozer.data)
        






































































































































































































































































































































































































